function generator(array, amount, id) {
    "use strict";
    var index,
        result = "",
        arr = array,
        i;

    for (i = 0; i < amount; i += 1) {
        index = Math.floor(Math.random() * arr.length);
        result += arr[index];
    }

    document.getElementById(id).innerHTML = result;
}

